# Escape sequences in strings
# Escape sequences allow you to include special characters in strings

course = 'Ultimate Python "Mastery"'
course2 = "Ultimate Python \"Mastery\""
course3 = 'Ultimate Python \'Mastery\''
course4 = 'Ultimate Python \nMastery'
print(course)
print(course2)
print(course3)
print(course4)
