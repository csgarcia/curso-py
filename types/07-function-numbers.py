import math

print(round(2.5))  # Rounding to nearest integer, Output: 3
print(abs(-2.5))  # Absolute value, Output: 2.5

print(math.ceil(2.5))  # Ceiling, Output: 3 round up to nearest integer
print(math.floor(2.5))  # Floor, Output: 2 round down to nearest integer
print(math.isnan(23))  # Check if NaN, Output: False
print(math.isnan(float('nan')))  # Check if NaN, Output: True
print(math.pow(10, 3))  # Power, Output: 1000.0
print(math.sqrt(16))  # Square root, Output: 4.0

